def multiply(a, b):
    """
    Multiply two numbers together.

    Args:
        a: First number
        b: Second number

    Returns:
        The product of a and b
    """
    return a * b


if __name__ == "__main__":
    # Test the function
    result = multiply(3, 5)
    print(f"3 * 5 = {result}")
